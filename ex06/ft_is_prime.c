int		ft_is_prime(int nb)
{
	int i;

	i = 2;
	if (nb == 1)
		return (0);
	if ((nb % 2 == 0 && nb != 2) || (nb % 3 == 0 && nb != 3) || (nb < 0))
		return (0);
	while (nb > i)
	{
		if (nb % i == 0)
			return (0);
		i = i + 1;
	}
	return (1);
}
